import { OrbitControls } from "@react-three/drei";
import { Canvas } from "@react-three/fiber";
import useMacBookViewer from "../hooks/useMacBookViewer";
import { MacBookModel14 } from "./models/Macbook-14";
// import useMacBookStore from "../store";

const ProductViewer = () => {
  const { color, scale, setColor, setScale } = useMacBookViewer();
  return (
    <section id="product-viewer">
      <h2>Take a closer look.</h2>

      <div className="controls ">
        <p className="info">
          MacBookPro {scale} in {color}
        </p>

        <div className="flex-center gap-5 mt-5">
          <div className="color-control">
            <div
              onClick={() => setColor("#adb5bd")}
              className={`bg-neutral-300  ${color == "#adb5bd" && "active"}`}
            />
            <div
              onClick={() => setColor("#2e2c2e")}
              className={`bg-neutral-900 ${color == "#2e2c2e" && "active"}`}
            />
          </div>

          <div className="size-control">
            <div
              onClick={() => setScale(0.06)}
              className={`${
                scale === 0.06
                  ? "bg-white text-black"
                  : "bg-transparent text-white"
              }`}
            >
              <p>14"</p>
            </div>
            <div
              onClick={() => setScale(0.08)}
              className={`${
                scale === 0.08
                  ? "bg-white text-black"
                  : "bg-transparent text-white"
              }`}
            >
              <p>16"</p>
            </div>
          </div>
        </div>
      </div>
      <p className="text-white text-4xl">Render Canvas</p>
      <Canvas id="canvas" camera={{position: [0,2,5], fov:50, near:0.1, far:100}}>
         <ambientLight intensity={10}/>
        {/* <Box position={[0, 0, 0]} scale={10 * scale} material-color={color}/> */}
        <MacBookModel14 scale={0.06} position={0,0,0} />
        <OrbitControls enableZoom={false}/>
      </Canvas>
    </section>
  );
};

export default ProductViewer;
